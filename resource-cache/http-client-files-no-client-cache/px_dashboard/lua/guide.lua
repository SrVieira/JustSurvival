local selected=1

ui.rendering["Przewodnik"], desc=function(a, mainA)
    local uid=getElementData(localPlayer, "user:uid")
    if(not uid)then return end

    a=a > mainA and mainA or a

    local texs=assets.textures["Przewodnik"]
    if(not texs or (texs and #texs < 1))then return false end

    -- header
    dxDrawText("Przewodnik", 426/zoom, 63/zoom, 0, 0, tocolor(200, 200, 200, a), 1, assets.fonts[5], "left", "top")
    dxDrawText("Dowiedz się najważniejszych informacji o serwerze.", 426/zoom, 93/zoom, 0, 0, tocolor(150, 150, 150, a), 1, assets.fonts[1], "left", "top")
    
    dxDrawText(guide[selected].name, 380/zoom, 143/zoom, sw, 0, tocolor(200, 200, 200, a), 1, assets.fonts[5], "center", "top")
    
    for i,v in ipairs(
        {
            {draw=guide[selected-1], id=selected-1, pos={380/zoom+237/zoom, 213/zoom+(257-206)/2/zoom, 366/zoom, 206/zoom}, a=100},
            {draw=guide[selected], id=selected, pos={380/zoom+((sw-380/zoom)-458/zoom)/2, 213/zoom, 458/zoom, 257/zoom}, post=true, a=255},
            {draw=guide[selected+1], id=selected+1, pos={sw-312/zoom-366/zoom+75/zoom, 213/zoom+(257-206)/2/zoom, 366/zoom, 206/zoom}, a=100},
        }
    )do
        if(v.draw)then
            local aa=v.a > a and a or v.a
            dxDrawImage(v.pos[1], v.pos[2], v.pos[3], v.pos[4], texs[2+v.id], 0, 0, 0, tocolor(255, 255, 255, aa), v.post)
        end
    end

    dxDrawText(guide[selected].desc, 380/zoom+120/zoom, sh-575/zoom, sw-120/zoom, 0, tocolor(200, 200, 200, a), 1, assets.fonts[2], "center", "top", false, true)

    if(guide[selected-1])then
        dxDrawImage((380/zoom+(sw-380/zoom)/2)-((26/zoom)*#guide)/2-138/zoom-30/zoom, sh-98/zoom, 138/zoom, 38/zoom, texs[1], 0, 0, 0, tocolor(255, 255, 255, a))
        dxDrawText(guide[selected-1].name, (380/zoom+(sw-380/zoom)/2)-((26/zoom)*#guide)/2-138/zoom-30/zoom, sh-98/zoom, (380/zoom+(sw-380/zoom)/2)-((26/zoom)*#guide)/2-138/zoom-30/zoom+100/zoom, 38/zoom+sh-98/zoom, tocolor(150, 150, 150, a), 1, assets.fonts[2], "right", "center")
        onClick((380/zoom+(sw-380/zoom)/2)-((26/zoom)*#guide)/2-138/zoom-30/zoom, sh-98/zoom, 138/zoom, 38/zoom, function()
            selected=selected-1
        end)
    end

    if(guide[selected+1])then
        dxDrawImage((380/zoom+(sw-380/zoom)/2)+((26/zoom)*#guide)/2+20/zoom, sh-98/zoom, 138/zoom, 38/zoom, texs[1], 180, 0, 0, tocolor(255, 255, 255, a))
        dxDrawText(guide[selected+1].name, (380/zoom+(sw-380/zoom)/2)+((26/zoom)*#guide)/2+20/zoom+38/zoom, sh-98/zoom, 138/zoom, sh-98/zoom+38/zoom, tocolor(150, 150, 150, a), 1, assets.fonts[2], "left", "center")
        onClick((380/zoom+(sw-380/zoom)/2)+((26/zoom)*#guide)/2+20/zoom, sh-98/zoom, 138/zoom, 38/zoom, function()
            selected=selected+1
        end)
    end

    for i=1,#guide do
        local sX=(26/zoom)*(i-1)
        dxDrawImage((380/zoom+(sw-380/zoom)/2)+sX-((26/zoom)*#guide)/2, sh-86/zoom, 13/zoom, 13/zoom, texs[2], 0, 0, 0, selected == i and tocolor(72,103,140, a) or tocolor(150, 150, 150, a))

        if(selected == i)then
            dxDrawText(i, (380/zoom+(sw-380/zoom)/2)+sX-((26/zoom)*#guide)/2, sh-61/zoom, 13/zoom+(380/zoom+(sw-380/zoom)/2)+sX-((26/zoom)*#guide)/2, 13/zoom, tocolor(200, 200, 200, a), 1, assets.fonts[2], "center", "top")
        end

        onClick((380/zoom+(sw-380/zoom)/2)+sX-((26/zoom)*#guide)/2, sh-86/zoom, 13/zoom, 13/zoom, function()
            selected=i
        end)
    end
end