animations.restoreFavourites = function()
    local xml = xmlLoadFile("favourite.xml", true)
    if not xml then return false end
    local favourites = xmlNodeGetChildren(xml)
    for i, v in pairs(favourites) do
        local categoryName = xmlNodeGetAttribute(v, "categoryName")
        local animationIndex = (xmlNodeGetAttribute(v, "animationIndex") and tonumber(xmlNodeGetAttribute(v, "animationIndex")))
        if not categoryName or not animationIndex then xmlDestroyNode(v) end
        if not animations.animationList[categoryName] or not animations.animationList[categoryName].animationList[animationIndex] then xmlDestroyNode(v) end
        animations.animationList[categoryName].animationList[animationIndex].isFavourite = true
    end
    xmlSaveFile(xml)
    xmlUnloadFile(xml)
end
addEventHandler("onClientResourceStart", getResourceRootElement(), animations.restoreFavourites)

animations.createCommands = function()
    for categoryName, v in pairs(animations.animationList) do
        for i, v in pairs(v.animationList) do
            addCommandHandler(v.command, function()
                animations.tryUseAnimation(categoryName, i)
            end)
        end
    end
end
addEventHandler("onClientResourceStart", getResourceRootElement(), animations.createCommands)