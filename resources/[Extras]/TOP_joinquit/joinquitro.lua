addEventHandler('onPlayerJoin',g_Root,
function ()
    local country = exports['admin']:getPlayerCountry(source)
    setElementData(source,'Country',country)
    outputChatBox('#ffffff* ' .. getPlayerName(source) .. '#00a2ff ha comenzado a sobrevivir. #00a2ff[#FFFFFF' .. countryNames[tostring(country)] .. '#00a2ff] ', getRootElement(), 2, 255, 2, true)
    end
)

addEventHandler('onPlayerChangeNick', g_Root,
    function(oldNick, newNick)
        outputChatBox('#fefefe* ' .. oldNick .. ' #ff0000 ahora se llama #fefefe' .. newNick, getRootElement(), 3, 180, 255, true)
    end
)
 
addEventHandler('onPlayerQuit', g_Root,
    function(reason)
        outputChatBox('#00a2ff[#FFFFFF' .. reason .. '#00a2ff] #fefefe* ' .. getPlayerName(source) .. ' #00a2ff ha dejado de sobrevivir.', getRootElement(), 255, 0, 0, true)
    end
)